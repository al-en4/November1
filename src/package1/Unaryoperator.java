package package1;

public class Unaryoperator {

	public static void main(String[] args) {
		//unary operators(++,--)
		//++ increment by 1
		//-- decrement by 1
		int a=10;
		System.out.println(a++);// 10
		System.out.println(a--);// 11
		System.out.println(a);//
		System.out.println(++a);//adds 11
		System.out.println(--a);//less 10
		

	}

}
